/** Convert a state value to its full name. If already a full name, returns as-is. */
export function getFullStateName(state: string): string {
  // Check if it's an abbreviation
  const upper = state.trim().toUpperCase();
  if (STATE_NAMES[upper]) {
    return STATE_NAMES[upper];
  }
  // Already a full name or unrecognized — return as-is
  return state;
}
